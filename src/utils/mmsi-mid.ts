export function mmsiToCountry(mmsi: string | undefined | null): string | undefined {
  if (!mmsi) return undefined;
  const s = String(mmsi).trim();
  if (s.length < 3) return undefined;
  const mid = s.slice(0, 3);
  if (!/^\d{3}$/.test(mid)) return undefined;
  return MID_TO_COUNTRY[mid];
}

